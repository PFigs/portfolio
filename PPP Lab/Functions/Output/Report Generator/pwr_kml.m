function pwr_kml(name,lla)
%makes a kml file for use in google earth
%input:  name of track, one matrix containing latitude and longitude
%usage:  pwr_kml('track5',latlon)

    header=['<?xml version="1.0" encoding="UTF-8"?>\n',...
            '<kml xmlns="http://www.opengis.net/kml/2.2">\n',...
            '<Document>\n',...
            '<name>Paths</name>\n',...
            '<Style id="yellowLineGreenPoly">\n',...
            '<LineStyle>\n',...
            '<color>7f00ffff</color>\n',...
            '<width>4</width>\n',...
            '</LineStyle>\n',...
            '<PolyStyle>\n',...
            '<color>7f00ff00</color>\n',...
            '</PolyStyle>\n',...
            '</Style>\n',...
            '<Placemark>\n',...
            '<name>Absolute Extruded</name>\n',...
            '<description>Transparent green wall with yellow outlines</description>\n',...
            '<styleUrl>#yellowLineGreenPoly</styleUrl>\n',...
            '<LineString>\n',...
            '<extrude>1</extrude>\n',...
            '<tessellate>1</tessellate>\n',...
            '<altitudeMode>absolute</altitudeMode>\n',...
            '<coordinates>\n'];

    footer=['</coordinates>\n',...
            '</LineString>\n',...
            '</Placemark>\n',...
            '</Document>\n',...
            '</kml>\n'];
    
    
    fid = fopen([name '.kml'], 'wt');
    fprintf(fid,header);
    assert(fid > 0);
    for k=1:size(lla,1)
        fprintf(fid, '%0.6f,%0.6f,%0.6f\n', lla(k,2), lla(k,1),lla(k,3));
    end
    fprintf(fid,footer);
    fclose(fid);
end